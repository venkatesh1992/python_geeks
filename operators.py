# arithmetic operators (+ - * / // **)
a = 9
b = 4
add = a + b
sub = a - b
div_floor = a // b
div_float = a / b
modulo_div = a % b
mul = a * b
power = a ** b

print(f"addition of {a} + {b} is {add}")
print(f"subtraction of {a} - {b} is {sub}")
print(f"floor division of {a} // {b} is {div_floor}")
print(f"floating(decimal) division of {a} / {b} is {div_float}")
print(f"modulo division(will give remainder) of {a} % {b} is {modulo_div}")
print(f"multiplication of {a} * {b} is {mul}")
print(f"power of {a} ** {b} is {power}")

